    // Consulta por Codigo
    private void runQueryCodigo() {
        try {
            ejecutaHQLQuery("SELECT m.codigo, m.nombre, p.nombre, m.precioActual, m.stock, "
                    + "m.descatalogado FROM Medicamento m INNER JOIN m.proveedor p where m.codigo ="
                    + "" + Integer.parseInt(jTextFieldCodigo.getText()));
        } catch (HibernateException he) {
            he.printStackTrace();
        }
    }

    // Consulta por Nombre
    public void runQueryNombre() {
        ejecutaHQLQuery("SELECT m.codigo, m.nombre, p.nombre, m.precioActual, m.stock, m.descatalogado "
                + "FROM Medicamento m "
                + "INNER JOIN m.proveedor p where m.nombre like '%" + jTextFieldNombre.getText() + "%'");
    }

    private void ejecutaHQLQuery(String hql) {

        try {
            Session session = NewHibernateUtil.getSessionFactory().openSession();
            session.beginTransaction();
            Query consulta = session.createQuery(hql);
            List resultList = consulta.list();
            mostrarResultado(resultList);
            session.getTransaction().commit();
        } catch (HibernateException he) {
            JOptionPane.showMessageDialog(null, he.getMessage().toString());
        }
    }

    private void mostrarResultado(List resultList) {
        Vector<String> tableHeaders = new Vector<String>();
        Vector tableData = new Vector();
        tableHeaders.add("Código");
        tableHeaders.add("Medicamento");
        tableHeaders.add("Proveedor");
        tableHeaders.add("Precio Actual");
        tableHeaders.add("Stock");
        tableHeaders.add("Descatalogado");

        for (Object obj : resultList) {
            Object[] rowArray = (Object[]) obj;
            Vector<Object> oneRow = new Vector<Object>();
            oneRow.add(rowArray[0]);  // Código
            oneRow.add(rowArray[1]);  // Nombre del medicamento
            oneRow.add(rowArray[2]);  // Nombre del proveedor
            oneRow.add(rowArray[3]);  // Precio actual
            oneRow.add(rowArray[4]);  // Stock
            oneRow.add(rowArray[5]);  // Descatalogado
            tableData.add(oneRow);
        }

        jTableTabla.setModel(new DefaultTableModel(tableData, tableHeaders));
    }

