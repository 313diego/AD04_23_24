    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed

        jTableTabla.setVisible(false);
        jTableTabla.getTableHeader().setVisible(false);

        if (jTextFieldCodigo.getText().equals("") && jTextFieldNombre.getText().equals("")) {
            mostrarTabla(false, true, "Debes introducir el código o, al menos, parte del nombre del medicamento");
        } else {
            if (!jTextFieldCodigo.getText().equals("")) {

                if (!jTextFieldCodigo.getText().matches("[0-9]+")) {
                    mostrarTabla(false, true, "Debres introducir un dígito numérico");
                } else {
                    runQueryCodigo();
                    if (jTableTabla.getRowCount() == 0) {
                        mostrarTabla(false, true, "NO HAY MEDICAMENTOS");
                    } else {
                        mostrarTabla(true, false, "");
                    }
                }
            }
            if (!jTextFieldNombre.getText().equals("")) {
                if (!jTextFieldNombre.getText().matches("[a-zA-Z]+")) {
                    mostrarTabla(false, true, "Debes introducir solo letras");
                } else {
                    runQueryNombre();
                    if (jTableTabla.getRowCount() == 0) {
                        mostrarTabla(false, true, "NO HAY MEDICAMENTOS");
                    } else {
                        mostrarTabla(true, false, "");
                    }
                }

            }
        }

    }

    private void mostrarTabla(Boolean tabla, Boolean mensaje, String error) {

        if (tabla) {
            jTableTabla.setVisible(true);
            jTableTabla.getTableHeader().setVisible(true);
        } else {
            jTableTabla.setVisible(false);
            jTableTabla.getTableHeader().setVisible(false);
        }

        if (mensaje) {
            jTextFieldExcepciones.setEnabled(true);
            jTextFieldExcepciones.setBackground(Color.red);
            jTextFieldExcepciones.setVisible(true);
        } else {
            jTextFieldExcepciones.setEnabled(false);
            jTextFieldExcepciones.setBackground(Color.LIGHT_GRAY);
            jTextFieldExcepciones.setVisible(false);
        }

        jTextFieldExcepciones.setText(error);
    }//GEN-LAST:event_jButtonBuscarActionPerformed

